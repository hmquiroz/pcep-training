#Pide al usuario que ingrese dos números enteros y muestra por pantalla la suma y la diferencia de ambos números utilizando operadores aritméticos.

numero1 = int(input("Por favor, ingrese el primer numero entero: "))
numero2 = int(input("Por favor, ingrese el segundo numero entero: "))

suma = numero1 + numero2
diferencia = numero1 - numero2

print("La suma de los numeros ingresados es:", suma)
print("La diferencia de los numeros ingresados es:", diferencia)